//understanding the switch case statements 

const month = 3;

switch(month){
    case 1 :
        console.log("january");
        break;
    case 2 :
        console.log("febuary");
        break;
    case 3 :
        console.log("march");
        break;
    case 4 :
        console.log("April");
        break;
    case 5 :
        console.log("MAy");
        break;
        default:
            console.log("no month found");
            break;

    
}
//once the case is matched and if there is not any  break statement it will execute all the script after it note except the default statement


 // there is such rule that only numembrs are used in the switch statement but we can also use String values and any other type

 